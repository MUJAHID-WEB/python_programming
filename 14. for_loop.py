####### For Loop : 

## range : start, stop, step

print(list(range(15)))  # strat from 0 and stop before 15
print(list(range(1, 15))) # strat from 1 and stop before 15
print(list(range(1, 15, 3)))  # strat from 1 and stop before 15 with 3 step gap


