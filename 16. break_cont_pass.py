######## Break : When no need to run code after getting the required result, that time use 'break'

for i in range(1,6):
    if i == 4:
        break
    print(i)
######## Continue : When need to skip conditioned result but proceed to last output, that time use 'continue'

for i in range(1, 10):
    if i ==6:
        continue
    print(i)
######## Pass : When function is written but condition will be written in future, that time use 'pass'

for i in range(1, 10):
    pass